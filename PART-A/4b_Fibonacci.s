     AREA PROG4,CODE,READONLY
N RN 1
X RN 2
Y RN 3
Z RN 4
ENTRY 
     MOV N,#10
     LDR R5,=TABLE
	 LDR R6,=NUM
	 LDRB N,[R6]
	 MOV X,#1
	 STRB X,[R5],#1
	 SUB N,N,#1
	 MOV X,#0
	 MOV Y,#1
	 MOV Z,#0
LOOP
    ADD Z,X,Y
	STRB Z,[R5],#1
	MOV X,Y
	MOV Y,Z
	SUBS N,N,#1
	BNE LOOP
GO   B GO
NUM DCB 0X0A
TABLE SPACE 60
	END